CREATE SCHEMA IF NOT EXISTS "We_FlAI"
SET search_path TO "We_FlAI"

CREATE TABLE IF NOT EXISTS "Aeroporti" (
  "Id_Aeroporto" BIGSERIAL PRIMARY KEY,
  "Cod_Iata"     CHAR(3) NOT NULL UNIQUE,
  "Citta"        TEXT NOT NULL,
  "Paese"        TEXT NOT NULL,

  CONSTRAINT "CHK_Cod_Iata_Format"
    CHECK ("Cod_Iata" ~ '^[A-Z]{3}$')
)

CREATE TABLE IF NOT EXISTS "Voli" (
  "Id_Volo"      BIGSERIAL PRIMARY KEY,
  "Compagnia"    TEXT NOT NULL,
  "Id_AptPTZ"    BIGINT NOT NULL,
  "Id_AptARR"    BIGINT NOT NULL,
  "DataPTZ"      DATE NOT NULL,
  "DataARR"      DATE NOT NULL,
  "OraPTZ"       TIME NOT NULL,
  "OraARR"       TIME NOT NULL,
  "Prezzo"       NUMERIC(10,2) NOT NULL,

  CONSTRAINT "FK_Voli_Aeroporto_Partenza"
    FOREIGN KEY ("Id_AptPTZ") REFERENCES "Aeroporti"("Id_Aeroporto")
    ON UPDATE CASCADE
    ON DELETE RESTRICT,

  CONSTRAINT "FK_Voli_Aeroporto_Arrivo"
    FOREIGN KEY ("Id_AptARR") REFERENCES "Aeroporti"("Id_Aeroporto")
    ON UPDATE CASCADE
    ON DELETE RESTRICT,

  CONSTRAINT "CHK_Aeroporti_Diversi"
    CHECK ("Id_AptPTZ" <> "Id_AptARR"),

  CONSTRAINT "CHK_Prezzo_Pos"
    CHECK ("Prezzo" >= 0),

  CONSTRAINT "CHK_Data_Arrivo_Non_Prima"
    CHECK ("DataARR" >= "DataPTZ")
)

CREATE TABLE IF NOT EXISTS "Prenotazioni" (
  "Id_Prenotazioni" BIGSERIAL PRIMARY KEY,
  "Id_Volo"         BIGINT NOT NULL,
  "Id_Documento"    TEXT NOT NULL,
  "NomeUtente"      TEXT NOT NULL,
  "CognomeUtente"   TEXT NOT NULL,
  "MailUtente"      TEXT NOT NULL,

  CONSTRAINT "FK_Prenotazioni_Volo"
    FOREIGN KEY ("Id_Volo") REFERENCES "Voli"("Id_Volo")
    ON UPDATE CASCADE
    ON DELETE RESTRICT,

  CONSTRAINT "CHK_Mail_Valida"
    CHECK ("MailUtente" ~* '^[A-Z0-9._%+-]+@[A-Z0-9.-]+\.[A-Z]{2,}$')
)

CREATE INDEX IF NOT EXISTS "IDX_Voli_Tratta_Data"
  ON "Voli" ("Id_AptPTZ", "Id_AptARR", "DataPTZ")

CREATE INDEX IF NOT EXISTS "IDX_Prenotazioni_IdVolo"
  ON "Prenotazioni" ("Id_Volo")

CREATE INDEX IF NOT EXISTS "IDX_Prenotazioni_Mail"
  ON "Prenotazioni" ("MailUtente")

